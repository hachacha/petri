<center> <h2>About</h2> </center>
<div id="because" class="notHead">
     <p>
          <span style="font-weight:bold;font-size:1.5em;">abstract</span>
     </p>
     <p>
          This is an anonymous social network that tries its best to follow a <a href="http://en.wikipedia.org/wiki/Rhizome_%28philosophy%29"> rhizomatic structure</a>
          in socialization and connectivity between both users and content. 
          It is most similar to image and text message boards like <a href="http://4chan.org">4chan</a>. The goals
          and longer description are below this abstract.

     </p>
	<p>
          <span style="font-weight:bold">The goal of this website is to create an environment where:</span>
	</p>
	<p>
		<ul>
			<li>deliberative and constructive conversations are encouraged</li>
			<li>there is a public responsibility for things shared and said</li>
			<li>originality is held higher than presence online</li>
			<li>social capital is created through interaction rather than through a front-stage appearance</li>
		</ul>
	</p>

	<p>
		<span style="font-weight:bold">constructive conversation</span>
	</p>
	<p>
		Modern forums and message boards, while living in the reality of <a href="http://en.wikipedia.org/wiki/Web_2.0#Characteristics" target="_blank">web 2.0</a>
		, still appear	and act in a <a href="http://auburnbusiness.com/images/message_board.jpg" target="_blank">1.0</a> 
          <a href ="http://i.imgur.com/jJDD9.png" target="_blank">format</a>. 1.0 is a static page created and curated by just one person (with the exception of the <a href="http://www.royalpetography.com/guestbook/index.php">guest book</a>).
          2.0 diverges from that and opens the page up to dynamic usage of many users. The many users are able to input new content and information to the page as they wish.
          While the content can always be new, the format of the webpages and particularly the conversations remain static, unchanging, responses to a single topic or original post. The users who
          participate are loudly disseminating their own content in response to a small nugget of information.
     </p>
     <p>  A conversation is not had in responding to a single post, or single comment. After about 50 posts, in a particular thread, there
		is little divergence in the sort of posts that one sees. There are few who actually take the time to go through each and every
		comment/post and get a gist of what is happening there. They then input their own comment ignoring what has taken place before they
		got there.
     </p>
     <p>
     	Conversations move and diverge from the original topic. Conversations change based upon thoughtful comments and useful insight.
     	If the format of modern forums and image/message boards are to continue in this way they will not actually reflect how conversation
     	useful or not is had. Netizens are coddled by this form of communication. One aspect of the Internet is that it is like a tool similar to the dictionary.
          The result of the invention of the dictionary was that it created a standard of communication-- it showed how to spell words, and suggested uses for words through which people were able to better understand
          one another. If we are in support of the current system of communication, briefly detailed above, how will accurate or intelligent communication be had?
          Whether we like it or not the Internet is <a href="http://www.internetworldstats.com/stats.htm">growing </a>
	     as an ubiquitous entity of our daily lives and is established as a great facilitator of information and communication.
	     It seems to be the new trend of quantity over quality in communications, but it does not have to be that way.
     </p>

     <p>
     	<span style="font-weight:bold">public responsibility & brief overview of how this works</span>
     </p>
     <p>
          It is the hope of this website that there will be a public responsibility that will be upkept by the users. It is public in the sense that the way that users use the website defines
          the cultural values of those users. The moderators/curators of the website are the users themselves. All users start out with the same amount of abilities/privileges when they register
          on the site. A user is able to unlock new abilities/privileges by gaining points from two places: the system, and other users. These new  abilities will change
          their influence and power on the network. Points are given to the users from the system based upon the amount of participation (posts) and the way in which they participate (type of
          post (uploaded file, link, text-only, etc.)). Other users are also able to vote on individual posts from other users and judge them on certain qualities they have. These include
          originality, humor, informativeness, and usefulness, each with a certain point value. 
     </p>
     <p>
          While the points reference to an individual user, they are also referencing a specific post which retains an individual point value based on the two sources of points. If the 
          individual post gets enough points in a particular conversation it is able to then become the title of that conversation along with two of the other most influential posts in a
          conversation. These deteriorate over time depending on how many people continue to vote for them, and therefore the titles will constantly be changing as well.
     </p>
     <p>
          The titles of the room will also be the things that are able to connect rooms together, and because the titles are constantly changing so are the room relations. The goal of this is
          to 1. present users with content and opinions that may be outside of their normal online session/experience and 2. allow users to have an effect on the system overall-- no matter what
          the current topic is or where the conversation is an individual's post can have an influence. This is a strange concept because the users are <u>anonymous</u>, however, there is still a "public"
          responsibility that goes as far as one individual post.
     </p>
     <p>
     	<span style="font-weight:bold">originality vs presence</span>
     </p>
     <p>
          Originality is one of the most valuable forms of social capital in today's world. The internet provides a near perfect platform for the circulation of ideas and access to content.
          Communications happen both at a technical and social level with extreme rapidity, so finding something that is  <a href="http://en.wikipedia.org/wiki/Sticky_content" target="_blank">sticky</a>,
     	recreatable, and understandable is the best way to share ideas.
     </p>
     <p>
          Sharing is easy. It takes just a few clicks to reappropriate something in its original position online to a place where you are broadcasting it to your own network of 
          <a href="http://en.wikipedia.org/wiki/Netizen">netizens</a>. Often when things are shared in this way the sharer does not provide a context or reasoning for why they have 
          reappropriated the content. Perhaps it's funny, perhaps it's informative, but the meaning is ambiguous and context is lost if it is not elaborated upon. 
     </p>
     <p>
          While the action is good for disseminating information broadly and creating an object/idea that is "sticky" overall, it is generally lazy and does not necessarily prompt response,
          only passive consumption from the other users. If we are to continue to only share, and not <a href="http://www.furtherfield.org/features/reviews/secret-war-between-uploading-and-downloading">create</a>
          new and original content, the overall process of creation and creativity will be <a href="http://en.wikipedia.org/wiki/Tragedy_of_the_commons">suffocated</a>, limited only to a few and
          the online experience will become only a hair less passive than a television experience. 
     </p>
     <p>
          This site hopes to encourage users to create new content, and have that content be celebrated not for the individual posting it but for the sake of the larger conversation. Quality
          should be held higher than quantity.
     </p>     
     <p>
          <span style="font-weight:bold">social capital through interaction</span>
     </p>
     <p>
          Current social media websites are profile-based for the most part. This means that there is a portion of the website that is devoted to a user's online identity showing things like the 
          frequency of their participation, how they look on the overall system, their number of connections to other users, etc. In other words it acts as a score board or a stats board that other
          users can see, compete with, and 
          <a href="http://www.google.com/url?sa=t&rct=j&q=&esrc=s&source=web&cd=6&ved=0CF0QFjAF&url=http%3A%2F%2Feprints.lancs.ac.uk%2F33407%2F1%2FFetishism_eprint.pdf&ei=tKw2UcOTAoS10QH6q4D4Dw&usg=AFQjCNHJ5GvUQ2MiRZq8V-xzA8vwIj2qnA&bvm=bv.43287494,d.dmQ&cad=rja">fetishize</a>. 
          This leads to flat interaction that have little to do with the actual person. Confliction between the actual person and the online person results in a sort of tentativeness
          that limits what a user wishes to say and their modes of expression. Stripping a user of this needless front-stage skin of an online profile in theory allows them to speak freely. Every
          user on this website will be anonymous to one another. <a href="http://en.wikipedia.org/wiki/YOYOW"> YOYOW</a>.
     </p>
     <p>
          Of course, this also allows them to act "inhuman"/impolite/irrationally to others and others' ideas. The same sort of rating system detailed above is implemented in a negative fashion
          for posts that are belonging to those who misuse the website and act against the ethics of the website. However, to gain the ability to give people negative votes, a user must run
          the gamut of the website. The ability for negative voting is the last privilege earned. The reason behind this is an attempt to allow the user to learn the difference between
          those who are truly <a href="http://i0.kym-cdn.com/photos/images/newsfeed/000/195/049/Bs2SMEGhw3.png?1320377877">hostile</a> and those who simply have an opposite 
          <a href="http://us.123rf.com/400wm/400/400/rehanqureshi/rehanqureshi1207/rehanqureshi120700080/14273326-group-of-young-businessmen-having-a-serious-and-intense-discussion-in-the-business-meeting.jpg"> opinion</a>. 
     </p>
      <p>
          <span style="font-weight:bold">Questions that this website hopes to answer:</span>
          <ul>
               <li>is it possible to have a constructive conversation online?</li>
               <li>how will netizens respond to this format?</li>
               <li>will people learn to be more "civilized" online here?</li>
               <li>is there a "natural" online standard of ethics?</li>
               <li>how do users interact while anonymous?</li>
		<li>will users feel a desire to interact without tangible identity or reputation</li>

          </ul>
     </p>
